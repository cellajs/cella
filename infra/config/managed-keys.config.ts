import { defineManagedKeys } from '../lib/managed-keys'

/**
 * The fork-owned managed-keys registry: scoped Scaleway IAM keys the infra CLI
 * can MINT on the operator's behalf and seed into runtime secrets without
 * the operator hand-creating a key in the console and pasting it. Mirrors the
 * other config files (data only); `lib/managed-keys.ts` owns the machinery and
 * fail-fast validation.
 *
 * A managed key is distinct from a plain `operator` runtime secret only in HOW
 * its value is produced: cella mints a `<slug>-<suffix>` IAM application + policy
 * (scoped to `permissionSets`) and writes the resulting key into the runtime
 * secret(s) named in `assign`. Those targets still live in runtime-secrets.config.ts
 * as `valueSource: 'operator'`: Pulumi owns their (empty) containers and they stay
 * settable by hand via "Manage runtime secrets"; minting is just an automated way
 * to fill them, and re-minting (rotation) is the same call.
 *
 * Minting is ALWAYS operator-confirmed, never silent. The AI key authorizes
 * billed inference, so its prompt defaults to "no".
 */
export const managedKeysConfig = defineManagedKeys({
  ai: {
    suffix: 'ai',
    label: 'AI worker (Scaleway Generative APIs / Inference)',
    appDescription: 'AI worker principal — Scaleway Generative APIs + Inference (consumed by the MCP/AI worker)',
    policyDescription: 'AI worker policy: Generative APIs + Inference (auto-generated by infra CLI)',
    permissionSets: ['GenerativeApisFullAccess', 'GenerativeApisModelAccess', 'InferenceFullAccess'],
    // Generative APIs are billed per use, so never mint silently. Default to no.
    prompt: { message: 'Mint a scoped Scaleway AI key now? (enables the MCP/AI worker; inference is billed)', default: false },
    // Scaleway Generative APIs authenticate with a single Bearer token = the key's secret half.
    assign: { secretKey: 'scwAiApiKey' },
  },
  s3: {
    suffix: 's3',
    label: 'Object Storage (private attachments)',
    appDescription: 'Application Object Storage principal — presign + read/write of app buckets',
    policyDescription: 'App Object Storage policy (auto-generated by infra CLI)',
    permissionSets: ['ObjectStorageFullAccess'],
    prompt: { message: 'Mint a scoped Scaleway Object Storage key now? (enables private attachment upload/presign)', default: false },
    // S3 needs both halves of the minted key pair.
    assign: { accessKey: 's3AccessKeyId', secretKey: 's3AccessKeySecret' },
  },
});
